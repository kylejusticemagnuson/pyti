    def test_money_flow_index_period_6(self):
        period = 6
        result = money_flow_index.money_flow_index(self.close_data, self.high_data, self.low_data, self.volume, period)
        np.testing.assert_allclose(result, self.mfi_period_6_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_money_flow_index_period_8(self):
        period = 8
        result = money_flow_index.money_flow_index(self.close_data, self.high_data, self.low_data, self.volume, period)
        np.testing.assert_allclose(result, self.mfi_period_8_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_money_flow_index_period_10(self):
        period = 10
        result = money_flow_index.money_flow_index(self.close_data, self.high_data, self.low_data, self.volume, period)
        np.testing.assert_allclose(result, self.mfi_period_10_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_money_flow_index_invalid_period(self):
        period = 128
        with self.assertRaises(Exception) as cm:
            money_flow_index.money_flow_index(self.close_data, self.high_data, self.low_data, self.volume, period)
        expected = "Error: data_len < period"
        self.assertEqual(str(cm.exception), expected)

    def test_money_flow_index_invalid_data(self):
        with self.assertRaises(Exception) as cm:
            money_flow_index.money_flow_index(self.close_data[:-1], self.high_data, self.low_data, self.volume, 6)
        expected = ("Error: mismatched data lengths, check to ensure that "
                    "all input data is the same length and valid")
        self.assertEqual(str(cm.exception), expected)
