    def test_macd_period_6_period_12(self):
        result = moving_average_convergence_divergence.moving_average_convergence_divergence(self.data, 6, 12)
        np.testing.assert_allclose(result, self.macd_period_6_period_12_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_macd_period_8_period_16(self):
        result = moving_average_convergence_divergence.moving_average_convergence_divergence(self.data, 8, 16)
        np.testing.assert_allclose(result, self.macd_period_8_period_16_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_macd_period_10_period_20(self):
        result = moving_average_convergence_divergence.moving_average_convergence_divergence(self.data, 10, 20)
        np.testing.assert_allclose(result, self.macd_period_10_period_20_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_macd_invalid_period_short(self):
        with self.assertRaises(Exception) as cm:
            moving_average_convergence_divergence.moving_average_convergence_divergence(self.data, 128, 1)
        expected = "Error: data_len < period"
        self.assertEqual(str(cm.exception), expected)

    def test_macd_invalid_period_long(self):
        with self.assertRaises(Exception) as cm:
            moving_average_convergence_divergence.moving_average_convergence_divergence(self.data, 1, 128)
        expected = "Error: data_len < period"
        self.assertEqual(str(cm.exception), expected)
