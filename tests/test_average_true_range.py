    def test_atr_period_6(self):
        period = 6
        result = average_true_range.average_true_range(self.close_data, self.high_data, self.low_data, period)
        np.testing.assert_allclose(result, self.atr_period_6_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_atr_period_8(self):
        period = 8
        result = average_true_range.average_true_range(self.close_data, self.high_data, self.low_data, period)
        np.testing.assert_allclose(result, self.atr_period_8_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_atr_period_10(self):
        period = 10
        result = average_true_range.average_true_range(self.close_data, self.high_data, self.low_data, period)
        np.testing.assert_allclose(result, self.atr_period_10_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_atr_invalid_period(self):
        period = 128
        with self.assertRaises(Exception) as cm:
            average_true_range.average_true_range(self.close_data, self.high_data, self.low_data, period)
        expected = "Error: data_len < period"
        self.assertEqual(str(cm.exception), expected)
