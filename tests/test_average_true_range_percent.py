    def test_atrp_period_6(self):
        period = 6
        result = average_true_range_percent.average_true_range_percent(self.close_data, self.high_data, self.low_data, period)
        np.testing.assert_allclose(result, self.atrp_period_6_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_atrp_period_8(self):
        period = 8
        result = average_true_range_percent.average_true_range_percent(self.close_data, self.high_data, self.low_data, period)
        np.testing.assert_allclose(result, self.atrp_period_8_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_atrp_period_10(self):
        period = 10
        result = average_true_range_percent.average_true_range_percent(self.close_data, self.high_data, self.low_data, period)
        np.testing.assert_allclose(result, self.atrp_period_10_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_atrp_invalid_period(self):
        period = 128
        with self.assertRaises(Exception) as cm:
            average_true_range_percent.average_true_range_percent(self.close_data, self.high_data, self.low_data, period)
        expected = "Error: data_len < period"
        self.assertEqual(str(cm.exception), expected)
