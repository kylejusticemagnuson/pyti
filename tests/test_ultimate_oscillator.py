    def test_buying_pressure(self):
        bp = ultimate_oscillator.buying_pressure(self.close_data,
                                                 self.low_data)
        np.testing.assert_allclose(bp, self.buying_pressure_expected,
                                   rtol=1e-9, equal_nan=True)

    def test_average_7(self):
        avg = ultimate_oscillator.average_7(self.close_data,
                                              self.high_data, self.low_data)
        np.testing.assert_allclose(avg, self.avg7_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_average_14(self):
        avg = ultimate_oscillator.average_14(self.close_data,
                                              self.high_data, self.low_data)
        np.testing.assert_allclose(avg, self.avg14_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_average_28(self):
        avg = ultimate_oscillator.average_28(self.close_data,
                                              self.high_data, self.low_data)
        np.testing.assert_allclose(avg, self.avg28_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_ultimate_oscillator(self):
        uo = ultimate_oscillator.ultimate_oscillator(self.close_data,
                                                     self.high_data,
                                                     self.low_data)
        np.testing.assert_allclose(uo, self.uo_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_uo_invalid_data(self):
        with self.assertRaises(Exception) as cm:
            ultimate_oscillator.ultimate_oscillator(self.close_data[:-1], self.high_data, self.low_data)
        expected = ("Error: mismatched data lengths, check to ensure that "
                    "all input data is the same length and valid")
        self.assertEqual(str(cm.exception), expected)
