    def test_ema_period_6(self):
        period = 6
        result = exponential_moving_average.exponential_moving_average(self.data, period)
        np.testing.assert_allclose(result, self.ema_period_6_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_ema_period_8(self):
        period = 8
        result = exponential_moving_average.exponential_moving_average(self.data, period)
        np.testing.assert_allclose(result, self.ema_period_8_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_ema_period_10(self):
        period = 10
        result = exponential_moving_average.exponential_moving_average(self.data, period)
        np.testing.assert_allclose(result, self.ema_period_10_expected, rtol=1e-9,
                                   equal_nan=True)

    def test_ema_invalid_period(self):
        period = 128
        with self.assertRaises(Exception) as cm:
            exponential_moving_average.exponential_moving_average(self.data, period)
        expected = "Error: data_len < period"
        self.assertEqual(str(cm.exception), expected)
